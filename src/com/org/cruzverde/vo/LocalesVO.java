package com.org.cruzverde.vo;

public class LocalesVO {
	
	
	String Departamento,Ciudad,Dirección,Nombre_Drogueria,Horario;
	Double Latitud,Longitud;

	public String getDepartamento() {
		return Departamento;
	}

	public void setDepartamento(String departamento) {
		Departamento = departamento;
	}

	public String getCiudad() {
		return Ciudad;
	}

	public void setCiudad(String ciudad) {
		Ciudad = ciudad;
	}

	public String getDirección() {
		return Dirección;
	}

	public void setDirección(String dirección) {
		Dirección = dirección;
	}

	public String getNombre_Drogueria() {
		return Nombre_Drogueria;
	}

	public void setNombre_Drogueria(String nombre_Drogueria) {
		Nombre_Drogueria = nombre_Drogueria;
	}

	public String getHorario() {
		return Horario;
	}

	public void setHorario(String horario) {
		Horario = horario;
	}

	public Double getLatitud() {
		return Latitud;
	}

	public void setLatitud(Double latitud) {
		Latitud = latitud;
	}

	public Double getLongitud() {
		return Longitud;
	}

	public void setLongitud(Double longitud) {
		Longitud = longitud;
	}
	


}
